	public void schematicOne(Player player, int amount) {
		ItemStack item = new ItemStack(Material.PAPER, amount);
		ItemMeta itemm = item.getItemMeta();
		itemm.setDisplayName(ChatColor.DARK_PURPLE + "Placeable Schematic" + ChatColor.WHITE + " 1");
		ArrayList<String> iteml = new ArrayList<String>();
		iteml.add(ChatColor.LIGHT_PURPLE + "=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=");
		iteml.add(ChatColor.WHITE + "[TBD]");
		iteml.add(ChatColor.DARK_PURPLE + "Warning: " + ChatColor.WHITE + "This item will spawn in facing");
		iteml.add(ChatColor.WHITE + "south");
		iteml.add(ChatColor.LIGHT_PURPLE + "=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=");
		itemm.setLore(iteml);
		itemm.addItemFlags(ItemFlag.HIDE_ATTRIBUTES);
		item.setItemMeta(itemm);

		player.getInventory().addItem(item);
	}

